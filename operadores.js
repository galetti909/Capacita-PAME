// Arithmetic Operators
var num1 = 10;
var num2 = 5;


// Adição e subtração
var soma = num1 + num2; 
// Valor final de soma: 15
var diferenca = num1 - num2; 
// Valor final de diferenca: 5

// Multiplicação, potenciação e divisão
var produto = num1 * num2; 
// Valor final de produto: 50
var potencia = num1 ** num2;
// Valor final de potencia: 100000
var quociente = num1 / num2;
// Valor final de quociente: 2

// Resto da divisão
var resto = num1 % num2;
// Valor final de resto: 0

// Comparação

num1 = 15;
num2 = 14;

// Igual (==)
var igual = num1 == num2;
// Valor final de igual: false

// Diferente (!=)
var diferente = num1 != num2;
// Valor final de diferente: true

// Maior que (>), menor que (<), maior ou igual a (>=), menor ou igual a (<=)
var maiorQue = num1 > num2;
// Valor final de maiorQue: true

var menorQue = num1 < num2;
// Valor final de maiorQue: false

var maiorOuIgual = num1 >= num2;
// Valor final de maiorOuIgual: true

var menorOuIgual = num1 <= num2;
// Valor final de menorOuIgual: false


//Operadores Lógicos

num1 = 3;
num2 = -3;

// E/AND (&&)
var resultado = num1 > 0 && num2 > 0;
// Valor final de resultado: false

// OU/OR (||)
var resultado = num1 > 0 || num2 > 0;
//Valor final de resultado: true



