// Variáveis são espaços na memória do computador onde guardamos valores.

var nome = "João";
// O comando acima atribui o valor "João" à variável nome, sempre que quisermos usar o valor "João" basta chamar a variável nome.

var nome_completo = nome + " Silva";
// Assim a variável nome_completo será João Silva.

console.log(nome_completo)
// console.log é um comando para exibir elementos no console/terminal. É um comando muito utilizado para testar resultados e valores.

var x;
// Na maioria das linguagens, as variáveis podem ser declaradas sem receberem um valor.

x = 5;
// Se ela já foi declarada, o prefixo (var) não deve ser escrito.
